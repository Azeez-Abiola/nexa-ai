export function loadRecentReactions(): string[] {
  try {
    const raw = localStorage.getItem(RECENT_REACTIONS_KEY);
    if (!raw) return [];
    const parsed = JSON.parse(raw);
    return Array.isArray(parsed) ? parsed.filter((e) => typeof e === "string") : [];
  } catch {
    return [];
  }
}

export function saveRecentReaction(emoji: string) {
  const prev = loadRecentReactions().filter((e) => e !== emoji);
  const next = [emoji, ...prev].slice(0, MAX_RECENT_REACTIONS);
  try {
    localStorage.setItem(RECENT_REACTIONS_KEY, JSON.stringify(next));
  } catch { /* ignore quota */ }
  return next;
}
